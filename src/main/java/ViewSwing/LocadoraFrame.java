    public void updateLocadoraCliente() {
        List<LocadoraEntity> locadoras = locadoraController.index();

        // Define o modelo de tabela com as colunas
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Nome");
        model.addColumn("CNPJ");
        model.addColumn("Email");
        model.addColumn("Telefone");

        for (LocadoraEntity locadora : locadoras) {
            model.addRow(new Object[]{locadora.getId(), locadora.getNome(), locadora.getCnpj(), locadora.getEmail(), locadora.getTelefone()});
        }

        TabelaGerenciarLocadoras.setModel(model);
    }
