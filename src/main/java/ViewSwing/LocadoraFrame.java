    private void VisualizarLocadoraBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VisualizarLocadoraBtnActionPerformed
        CardPanel.removeAll();
        CardPanel.add(VisualizaLocadora);
        CardPanel.repaint();
        CardPanel.revalidate();

        List<LocadoraEntity> locadoras = locadoraController.index();

        // Define o modelo de tabela com as colunas
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Nome");
        model.addColumn("CNPJ");
        model.addColumn("Telefone");
        model.addColumn("Email");

        for (LocadoraEntity locadora : locadoras) {
            model.addRow(new Object[]{locadora.getId(), locadora.getNome(), locadora.getCnpj(), locadora.getTelefone(), locadora.getEmail()});
        }
        System.out.println(model);
        ListarLocadoras.setModel(model);
    }//GEN-LAST:event_VisualizarLocadoraBtnActionPerformed

