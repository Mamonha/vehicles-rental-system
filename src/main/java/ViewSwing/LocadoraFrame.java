    private void RemoverLocadoraBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoverLocadoraBtnActionPerformed
        int colunaSelected = TabelaGerenciarLocadoras.getSelectedRow();

        Object valorColuna = TabelaGerenciarLocadoras.getValueAt(colunaSelected,1);
        Long idLocadora = (long)TabelaGerenciarLocadoras.getValueAt(colunaSelected,0);

        if(valorColuna != null){
            ModalRemoverCliente modalRemover = new ModalRemoverCliente(this);
            modalRemover.setVisible(true);
            if(modalRemover.isConfirmado()){
                locadoraController.delete(locadoraController.findById(idLocadora));
                JOptionPane.showMessageDialog(this, "Locadora deletada com sucesso!");
                modalRemover.setVisible(false);
                repaint();
            }else{
                modalRemover.setVisible(false);
            }

        }
    }//GEN-LAST:event_RemoverLocadoraBtnActionPerformed

